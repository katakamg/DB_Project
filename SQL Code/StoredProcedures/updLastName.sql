CREATE PROCEDURE [dbo].[updLastName]
	
	@lastName VARCHAR(64),
	@netID char(10)

AS

BEGIN TRANSACTION

UPDATE people SET lastName = @lastName WHERE NetID = @netID

IF (@@ERROR <> 0)
  BEGIN
	PRINT 'ERROR OCCURRED WHILE ATTEMPTING TO UPDATE DATA IN people'
	ROLLBACK TRANSACTION
	RETURN -10001
  END

COMMIT TRANSACTION

RETURN


GO


